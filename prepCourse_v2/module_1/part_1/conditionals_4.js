


function isOdd(num) {
  return num % 2 === 1; 
}



function isSameLength(word1, word2) {
  return word1.length === word2.length; 
}


function areBothOdd(num1, num2) {
  return num1 % 2 === 1 && num2 % 2 === 1; 
}


function isEitherEven(num1, num2) {
  return num1 % 2 === 0 || num2 % 2 === 0;
}